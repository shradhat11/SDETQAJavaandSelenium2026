package com.javabasics.day3;

public class AssignmentOperator {
    public static void main(String []args){

        //Assignment operator = += -+ *= /= %=

        int a=10;
        a+=5; //a=a+5
        System.out.println(a);
        a-=5;//a=a-5
        System.out.println(a);

        int b=10;
        b*=2;
        System.out.println(b);
        b/=2;
        System.out.println(b);
        b%=2;
        System.out.println(b);
    }
}
