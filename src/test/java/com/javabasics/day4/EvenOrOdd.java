package com.javabasics.day4;

public class EvenOrOdd {
    public static void main(String []args){

        int a=11;
        if(a%2 == 0)
        {
            System.out.println("The Number "+a+" is an even number");
        }
        else
        {
            System.out.println("The Number "+a+" is an odd number");
        }
    }
}
