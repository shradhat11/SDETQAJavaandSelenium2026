package com.javabasics.day2;

public class DataTypesDemo {
    public static void main(String []args){

        //Numeric datatypes
        int a=100,b=200;
        System.out.println("The value of a : "+a);
        System.out.println("The value of b : "+b);
        System.out.println("The sum of a and b :"+(a+b));

        byte c=125;
        System.out.println("The value of c :"+c);

        short sh=3535;
        System.out.println("The value of sh :"+sh);

        long lg=434342523l;  //literal is needed :l or L
        System.out.println("The value of lg :"+lg);

        //Decimal number

        float f= 2.13f; //Literal is needed :f or F
        System.out.println(f);
        double d=444.2277777;
        System.out.println(d);

        char grade='A';
        System.out.println(grade);

        String name="Tony";
    System.out.println(name);
    }
}
